    private void jTableProdukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableProdukMouseClicked
        // TODO add your handling code here:
        int row = jTableProduk.getSelectedRow();

        String name = jTableProduk.getModel().getValueAt(row, 0).toString();
        String price = jTableProduk.getModel().getValueAt(row, 1).toString();
        String desc = jTableProduk.getModel().getValueAt(row, 2).toString();
        String status = jTableProduk.getModel().getValueAt(row, 3).toString();

        tfNamaProduk.setText(name);
        tfHargaProduk.setText(String.valueOf(price));
        jTextAreaDetail.setText(desc);

        int is_available = switch (status) {
            case "Tersedia" -> 0;
            case "Kosong" -> 1;
            default -> 1;
        };

        jComboBoxStatus.setSelectedIndex(is_available);
    }//GEN-LAST:event_jTableProdukMouseClicked

